package curs3;

public class IncrementDecrementExample {

	public static void main(String[] args) {

		int number = 10;
		
		//System.out.println(number = number+1);
		
		//System.out.println(++number); //preincrement

		System.out.println(number++); //postincrement
		
		System.out.println(number);
		
		//System.out.println(--number); //predecrement
		
		System.out.println(number--); //postdecrement
		
		System.out.println(number);
		
	}

}
